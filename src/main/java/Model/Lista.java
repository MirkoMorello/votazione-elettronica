package Model;

public class Lista {
	
	private String nome;
	private String descrizione;
	
	public Lista(String nome, String descrizione) {
		this.nome = nome;
		this.descrizione = descrizione;
	}
	
	public String getName() {
		return this.nome;
	}
	
	public String getDesc() {
		return this.descrizione;
	}
}
